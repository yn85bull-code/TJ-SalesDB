const workbook = Workbook.create();

function safeSheetName(name) {
  return name.replace(/[\\/?*:[\]]/g, "_").slice(0, 31);
}

function buildSheet(name, dataRows) {
  const sheet = workbook.worksheets.add(safeSheetName(name));
  const values = [headers, ...dataRows];
  const endRow = values.length;
  const endCol = headers.length;
  sheet.getRange(`A1:M${endRow}`).values = values;
  sheet.freezePanes.freezeRows(1);
  sheet.getRange("A1:M1").format = {
    fill: "#111318",
    font: { color: "#FFFFFF", bold: true },
    horizontalAlignment: "center",
    verticalAlignment: "center",
    wrapText: true
  };
  sheet.getRange(`A2:M${endRow}`).format = {
    borders: { preset: "all", style: "thin", color: "#D1D5DB" },
    verticalAlignment: "top",
    wrapText: true
  };
  sheet.getRange(`G2:J${endRow}`).format.fill = "#FFF2CC";
  sheet.getRange(`L2:L${endRow}`).format.fill = "#FCE4D6";
  sheet.getRange("A:A").format.columnWidthPx = 46;
  sheet.getRange("B:B").format.columnWidthPx = 96;
  sheet.getRange("C:C").format.columnWidthPx = 130;
  sheet.getRange("D:D").format.columnWidthPx = 150;
  sheet.getRange("E:E").format.columnWidthPx = 76;
  sheet.getRange("F:F").format.columnWidthPx = 230;
  sheet.getRange("G:G").format.columnWidthPx = 150;
  sheet.getRange("H:H").format.columnWidthPx = 118;
  sheet.getRange("I:I").format.columnWidthPx = 150;
  sheet.getRange("J:J").format.columnWidthPx = 210;
  sheet.getRange("K:K").format.columnWidthPx = 96;
  sheet.getRange("L:L").format.columnWidthPx = 72;
  sheet.getRange("M:M").format.columnWidthPx = 260;
  sheet.getRange(`A1:M${endRow}`).format.autofitRows();
  return sheet;
}

